let abrir = document.querySelector('.abrir');
let r = document.querySelector('.R');
let z = document.querySelector('.z');

/*???????????????????????????????????????????????

				Desaparece/Aparece ao clicar no icone

?????????????????????????????????????????????????*/

function Desaparece() {
	abrir.style.opacity = '0';
	abrir.style.height = '0';	
}

function AbrirNav(){
	if((abrir.style.opacity) == '0' && (abrir.style.opacity) == '0'){
	abrir.style.opacity = '1';
	abrir.style.height = '300px';	
				   } else {
				   	Desaparece();
				   				}
}

/*?????????????????????????????????????????

	Verificador De Janela para Cada Ação Scroll

??????????????????????????????????????????*/

window.addEventListener("scroll", function(){
	let bloco = (document.querySelector('.block'));
	let conjunto = (document.querySelector('.conjunto'));

	if((window.scrollY) < 20){
		bloco.style.width = '0px';
		bloco.style.height = '0px';
		conjunto.style.display = 'none';
		Desaparece();

	} else {
		bloco.style.width = '60px';
		bloco.style.height = '60px';
		conjunto.style.display = 'flex';
	}
} );


